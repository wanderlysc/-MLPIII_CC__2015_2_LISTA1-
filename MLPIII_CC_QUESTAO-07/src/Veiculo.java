
public class Veiculo {
	private String placa, marca, modelo;
	private int ano;
	private float valorKmRodado, kmInicial, kmFinal, valorLocacao;
	
	public String getPlaca() {
		return placa;
	}
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public float getValorKmRodado() {
		return valorKmRodado;
	}
	public void setValorKmRodado(float valorKmRodado) {
		this.valorKmRodado = valorKmRodado;
	}
	public float getKmInicial() {
		return kmInicial;
	}
	public void setKmInicial(float kmInicial) {
		this.kmInicial = kmInicial;
	}
	public float getKmFinal() {
		return kmFinal;
	}
	public void setKmFinal(float kmFinal) {
		this.kmFinal = kmFinal;
	}
	public float getValorLocacao() {
		return valorLocacao;
	}
	public void setValorLocacao(float valorLocacao) {
		this.valorLocacao = valorLocacao;
	}
	
	
}
